package com.a158;

public class DataTypeEx 
{
	
	public static void main(String[] args)
	{
		
		
		int a = 6;
		int b = 2;
		
		b+=a;//b=b+a
		System.out.println(b);//8
		
		b-=a;//b = b-a
		System.out.println(b);		
		
		b*=a;
		System.out.println(b);
		
		b/=a;
		System.out.println(b);
		
		b%=a;
		System.out.println(b);

		
		
		
	}
	
}
