package com.a158;

public class DataTypeEx2 
{
	
	public static void main(String[] args)
	{
		// +, - , *, / , %
		
		int a = 6;
		int b = 5;
		
		int add = a+b;
		System.out.println("Sum of addition is " + add);
		
		int sub = a-b;
		System.out.println(sub);
		
		int mul = a*b;
		System.out.println(mul);
		
		int div = a/b;
		System.out.println(div);
		
		int mod = a%b;
		System.out.println(mod);//remainder
		
		
		
		
	}
	
}
