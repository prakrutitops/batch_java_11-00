package com.a29;

public class StringEx 
{
	public static void main(String[] args) {
		
		//literal string
		String s1 ="sachin";
		
		//object string
		String s2 =new String("sachin");
		
		//character array string
		char ch[]= {'H','E','L','L','O'};
		String s3 =new String(ch);
		
		System.out.println(s1);
		System.out.println(s2);
		System.out.println(s3);
		
	}
}
